package first_homework;

public class arithmetic_operations {
    public static void main(String[] args) {
        byte b = 5;
        short s = 6;
        int i = 7;
        long l = 8;
        float f = 3.0F;
        double d = 9.0D;
        System.out.println(b + s);
        System.out.println(i - l);
        System.out.println(d / f);
        System.out.println(b * i);

    }
}
